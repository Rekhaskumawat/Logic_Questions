class Pattern
{
    int iNo ;

    public Pattern(int iNo)
    {
        this.iNo = iNo ;
    }

    public void pattern()
    {
        int iCnt = 0 ;

        for(iCnt = iNo ; iCnt >= 1 ; iCnt-- )
        {
            System.out.print(iCnt+"\t#\t");
        }
    }
}//end of Pattern class

