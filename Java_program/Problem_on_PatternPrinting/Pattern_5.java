class Pattern
{
    int iNo ;

    public Pattern(int iNo)
    {
        this.iNo = iNo ;
    }

    public void pattern()
    {
        int iCnt = 0 ;

        for(iCnt = 1 ; iCnt <= 2*iNo ; iCnt++ )
        {
            if((iCnt % 2 ) == 0)
            {
                System.out.print(iCnt+"\t");
            }
            
        }
    }
}//end of Pattern class

