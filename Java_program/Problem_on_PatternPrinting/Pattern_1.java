class Pattern
{
    int iNo ;

    public Pattern(int iNo)
    {
        this.iNo = iNo ;
    }

    public void pattern()
    {
        int iCnt = 0 ;
        char ch = 'A' ;

        for(iCnt = 1 ; iCnt <= iNo ; iCnt++ ,ch++ )
        {
            System.out.print(ch+ "\t");
        }
    }
}//end of Pattern class

