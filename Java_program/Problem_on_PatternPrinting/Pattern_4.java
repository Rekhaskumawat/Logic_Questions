class Pattern_4
{
    public static void main(String A[])
    {
        int iValue = 0 ;

        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter the number:");
        iValue = sobj.nextInt();

        Pattern pobj = new Pattern(iValue);
        pobj.pattern();
    }
}// end of main class

