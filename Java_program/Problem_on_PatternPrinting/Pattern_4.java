class Pattern
{
    int iNo ;

    public Pattern(int iNo)
    {
        this.iNo = iNo ;
    }

    public void pattern()
    {
        int iCnt = 0 ;

        for(iCnt = 1 ; iCnt <= iNo ; iCnt++ )
        {
            System.out.print("#\t"+iCnt+"\t*\t");
        }
    }
}//end of Pattern class

