class Pattern
{
    public: 

        int iNo ;

        Pattern(int iNo)
        {
            this->iNo = iNo ;
        }

        void pattern()
        {
            int iCnt = 0 ;
            char ch = 'A' ;

            for(iCnt = 1 ; iCnt <= iNo ; iCnt++ ,ch++ )
            {
                cout<<ch<<"\t";
            }
        }
};//end of Pattern class

//////////////////////////////////////////////////////////////////////////////////////////////
//
//Entry point function
//
//////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0 ;

    cout<<"enter the number :";
    cin>>iValue;

    Pattern pobj(iValue);
    pobj.pattern();

}// end of main function

