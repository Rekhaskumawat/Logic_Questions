class Pattern
{
    public: 

    int iNo ;
    Pattern(int iNo)
    {
        this->iNo = iNo ;
    }
    void pattern()
    {
        int iCnt = 0 ;

        for(iCnt = 1 ; iCnt <= 2*iNo ; iCnt++ )
        {
            if((iCnt % 2 ) == 0)
            {
                cout<<iCnt<<"\t";
            }
            
        }
    }
};//end of Pattern class

//////////////////////////////////////////////////////////////////////////////////////////////
//
//Entry point function
//
//////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0 ;

    cout<<"enter the number :";
    cin>>iValue;

    Pattern pobj(iValue);
    pobj.pattern();

}// end of main function

