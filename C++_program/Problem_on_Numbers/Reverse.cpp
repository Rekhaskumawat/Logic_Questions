class Logic
{
    int iNo ;
    public:
    Logic(int iNo)
    {
        this->iNo = iNo;
    }
    void reverse()
    {
        int iDigit = 0 ;
        int iRev = 0 ;
        while(iNo != 0 )
        {
            iDigit = iNo % 10 ;
            iNo = iNo /10 ;
            iRev = iRev*10 + iDigit;
        }
       cout<<"Reversed Number : "<<iRev<<"\n";
    }
};// end of Logic Class

//////////////////////////////////////////////////////////////////////////////////////////////
//
//Entry point function
