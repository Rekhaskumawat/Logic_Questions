class Logic
{
    public:
        int iNo ;

        Logic(int iNo)
        {
            this->iNo = iNo;
        }
    
        void Factor()
        {
            int iCnt = 0 ;
            for(iCnt = 1 ; iCnt <= iNo ; iCnt++)
            {
                if((iNo % iCnt) == 0)
                {
                    cout<<iCnt<<"\t";
                }
            }
        }
};//end of the Logic class

