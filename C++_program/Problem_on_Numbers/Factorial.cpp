class Logic
{
    public:
        int iNo ;

        Logic(int iNo)
        {
            this->iNo = iNo;
        }
    
        int Factorial()
        {
            int iCnt = 0 ;
            int iFact = 1 ;
            for(iCnt = 1 ; iCnt <= iNo ; iCnt++)
            {
                iFact = iFact * iCnt;
            }
            return iFact;
        }
};//end of the Logic class

//////////////////////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
//////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iValue = 0 ;
    int iRet = 0 ;
    
    cout<<"enter the number :\n";
    cin>>iValue;

    Logic lobj(iValue);
    iRet = lobj.Factorial();

    cout<<"Factorial of "<<iValue<<" is : "<<iRet<<"\n";

    return 0 ;
}//end of main function

