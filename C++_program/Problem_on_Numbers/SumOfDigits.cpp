class Logic
{
    int iNo ;
    public:
    Logic(int iNo)
    {
        this->iNo = iNo;
    }
    int sumOfDigits()
    {
        int iDigit = 0 ;
        int iSum = 0 ;
        while(iNo != 0 )
        {
            iDigit = iNo % 10 ;
            iNo = iNo /10 ;
            iSum = iSum +iDigit;
        }
       return iSum ;
    }
};// end of Logic Class

