class Logic
{
    int iNo ;
    public:
    Logic(int iNo)
    {
        this->iNo = iNo;
    }
    int productOfDigits()
    {
        int iDigit = 0 ;
        int iProd = 1 ;
        while(iNo != 0 )
        {
            iDigit = iNo % 10 ;
            iNo = iNo /10 ;
            iProd = iProd*iDigit;
        }
       return iProd ;
    }
};// end of Logic Class

//////////////////////////////////////////////////////////////////////////////////////////////
//
//Entry point function
//
//////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iRet = 0 ;
    int iValue = 0 ;
    cout<<"Enter the number:\n";
    cin>>iValue;

    Logic lobj(iValue);
    iRet = lobj.productOfDigits();

    cout<<"Product of Digits of number is :"<<iRet<<"\n";
}//end of main function

