void reverse(int iNo)
{
    int iDigit = 0 ;
    int iRev = 0 ;
    while(iNo != 0 )
    {
        iDigit = iNo % 10 ;
        iNo = iNo /10 ;
        iRev = iRev*10 + iDigit;
    }
    printf("Reversed number :%d\n",iRev);
}// end of reverse function

//////////////////////////////////////////////////////////////////////////////////////////////
//
//Entry point function
//
//////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0 ;

    printf("Enter the number :\n");
    scanf("%d",&iValue);

    reverse(iValue);

}//end of main function

