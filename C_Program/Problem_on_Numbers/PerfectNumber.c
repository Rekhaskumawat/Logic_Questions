int main()
{
    int iValue = 0 ;
    int iRet = 0 ;
    
    printf("enter the number :" );
    scanf("%d", &iValue);

    iRet = PerfectNumber(iValue);

    if(iRet == iValue)
    {
        printf("%d is  perfect number" , iValue);
    }
    else
    {
       printf("%d is not  perfect number", iValue);
    }

    return 0 ;
}//end of main function

