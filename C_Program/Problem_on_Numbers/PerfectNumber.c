int PerfectNumber(int iNo)
{
    int iCnt = 0 ;
    int iSum = 0 ;
    for(iCnt = 1 ; iCnt < iNo ; iCnt++)
    {
        if((iNo % iCnt) == 0)
        {
            iSum = iSum + iCnt;
        }
    }
    return iSum;
}
//end of the PerfectNumber function

