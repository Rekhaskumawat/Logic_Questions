int largestDigit(int iNo)
{
    int ilargest = 0 ;
    int iDigit = 0 ;
    while(iNo != 0 )
    {
        iDigit = iNo % 10 ;
        iNo = iNo /10 ;
        if(ilargest <= iDigit)
        {
            ilargest = iDigit ;
        }
    }
    return ilargest;
}// end of latgestDigit function

//////////////////////////////////////////////////////////////////////////////////////////////
//
//Entry point function
//
//////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iRet = 0 ;
    int iValue = 0 ;
    
    printf("Enter the number :\n");
    scanf("%d",&iValue);

    iRet =largestDigit(iValue);

    printf("largest Digit is: %d\n", iRet);

}//end of main function

