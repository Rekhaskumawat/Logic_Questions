int sumOfDigits(int iNo)
{
    int iDigit = 0 ;
    int iSum = 0 ;
    while(iNo != 0 )
    {
        iDigit = iNo % 10 ;
        iNo = iNo /10 ;
        iSum = iSum +iDigit;
    }
    return iSum ;

}// end of sumOfDigits function

