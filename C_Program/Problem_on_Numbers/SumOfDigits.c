//
//Entry point Function
//
//////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iRet = 0 ;
    int iValue = 0 ;
    
    printf("Enter the number :\n");
    scanf("%d",&iValue);

    iRet =sumOfDigits(iValue);

    printf("sum of Digitts of number is : %d\n", iRet);
}//end of main function

