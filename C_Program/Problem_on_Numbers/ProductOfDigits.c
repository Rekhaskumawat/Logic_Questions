int productOfDigits(int iNo)
{
    int iDigit = 0 ;
    int iProd = 1 ;
    while(iNo != 0 )
    {
        iDigit = iNo % 10 ;
        iNo = iNo /10 ;
        iProd = iProd*iDigit;
    }
    return iProd ;

}// end of productOfDigits

//////////////////////////////////////////////////////////////////////////////////////////////
//
//Entry point function
//
//////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iRet = 0 ;
    int iValue = 0 ;

    printf("Enter the number :\n");
    scanf("%d",&iValue);

    iRet = productOfDigits(iValue);

   printf("product of digits of number: %d\n",iRet);
   
}//end of main function

