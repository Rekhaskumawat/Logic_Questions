int Factorial(int iNo)
{
    int iCnt = 0 ;
    int iFact = 1 ;
    for(iCnt = 1 ; iCnt <= iNo ; iCnt++)
    {
        iFact = iFact * iCnt;
    }
    return iFact;
}
//end of the Factrial function

//////////////////////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
//////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iValue = 0 ;
    int iRet = 0 ;
    
    printf("enter the number :" );
    scanf("%d", &iValue);

    iRet = Factorial(iValue);

    printf("Factorial of %d is : %d", iValue, iRet);

    return 0 ;
}//end of main function

