void Factor(int iNo)
{
    int iCnt = 0 ;
    for(iCnt = 1 ; iCnt <= iNo ; iCnt++)
    {
        if((iNo % iCnt) == 0)
        {
            printf("%d\t", iCnt);
        }
    }
}
//end of the Factor function

