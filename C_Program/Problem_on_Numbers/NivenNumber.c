//
//  Entry point function
//
//////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    
    bool bRet = false ;
    int iValue = 0 ;

    printf("Enter the number :\n");
    scanf("%d",&iValue);

    bRet =nivenNumber(iValue);

    if(bRet == true)
    {
        printf("%d is Niven number\n", iValue);
    }
    else
    {
        printf("%d is not Niven number\n", iValue);
    }

}//end of main function

