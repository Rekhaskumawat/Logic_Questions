bool nivenNumber(int iNo)
{
    int iDigit = 0 ;
    int iSum = 0 ;
    int itemp = iNo;
    while(iNo != 0 )
    {
        iDigit = iNo % 10 ;
        iNo = iNo /10 ;
        iSum = iSum + iDigit;
    }
    if((itemp % iSum) == 0)
    {
        return true;
    }
    else
    {
        return false;
    }
    
}// end of nivenNumber function

