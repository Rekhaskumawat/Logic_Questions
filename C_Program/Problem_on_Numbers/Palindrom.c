int palindrom(int iNo)
{
    int iDigit = 0 ;
    int iRev = 0 ;
    while(iNo != 0 )
    {
        iDigit = iNo % 10 ;
        iNo = iNo /10 ;
        iRev = iRev*10 + iDigit;
    }
    return iRev;
}// end of palindrom function

//////////////////////////////////////////////////////////////////////////////////////////////
//
//Entry point function
//
//////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iRet = 0 ;
    int iValue = 0 ;
    
    printf("Enter the number :\n");
    scanf("%d",&iValue);

    iRet =palindrom(iValue);

    if(iRet == iValue)
    {
        printf("%d is a palindrom number\n",iValue);
    }
    else
    {
         printf("%d is not a palindrom number\n",iValue);
    }

}
//end of main function

