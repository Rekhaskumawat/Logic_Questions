void pattern(int iNo)
{
    int iCnt = 0 ;

    for(iCnt = 1 ; iCnt <= 2*iNo ; iCnt++ )
    {
        if((iCnt % 2 ) == 0)
        {
            printf("%d\t",iCnt);
        }
        
    }
}//end of Pattern function

///////////////////////////////////////////////////////////////////////////////////////////////
//
//Entry point function
//
//////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0 ;

    printf("Enter the Number:\n");
    scanf("%d", &iValue);
    
    pattern(iValue);

}// end of main function

