void pattern(int iNo)
{
    int iCnt = 0 ;
    char ch = 'A' ;

    for(iCnt = 1 ; iCnt <= iNo ; iCnt++ ,ch++ )
    {
        printf("%c\t",ch);
        
    }

}//end of Pattern function

//////////////////////////////////////////////////////////////////////////////////////////////
//
//Entry point function
//
//////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0 ;

    printf("Enter the Number:\n");
    scanf("%d", &iValue);
    
    pattern(iValue);

}// end of main function

